package com.xjx.springboottest;

public class test_i {
    public static void main(String[] args) {
        int i = 1;
        System.out.println(i++);//1
        System.out.println(++i);//3
        System.out.println(i--);//3
        System.out.println(--i);//1
        System.out.println(i);//0
    }
}
